package com.zdsoft.finance.customer.vo;

/**
 * 下拉框值设定VO
 * @author zhangchao
 *
 */
public class RelationshipTypeVo {

	//事件编码
	private String eventsCd;
	
	//事件名称
	private String eventsName;

	public String getEventsCd() {
		return eventsCd;
	}

	public void setEventsCd(String eventsCd) {
		this.eventsCd = eventsCd;
	}

	public String getEventsName() {
		return eventsName;
	}

	public void setEventsName(String eventsName) {
		this.eventsName = eventsName;
	}
}
